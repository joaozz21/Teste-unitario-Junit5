class Calculadora() {
    val calculadora = Calculadora()

    companion object {
        fun soma(x: Double, y: Double):Double {
            return  x + y

        }

        fun sub(x: Double, y: Double):Double {
            return x-y
        }


        fun div(x: Double, y: Double):Double {
           return x/y
        }

        fun mult(x: Double, y: Double):Double {
            return x*y
        }
    }
}